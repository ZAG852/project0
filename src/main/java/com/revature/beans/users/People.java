package com.revature.beans.users;

public class People {
	private int id;
	private String username;
	private String password;
	String getPassword() {
		return password;
	}
	void setPassword(String password) {
		this.password = password;
	}
	String getUsername() {
		return username;
	}
	void setUsername(String username) {
		this.username = username;
	}
	int getId() {
		return id;
	}
	void setId(int id) {
		this.id = id;
	}
	

}
